export const citiesForSelect = (list) => {
  const groupedCities = {};
  list.forEach((city) => {
    const { country, code, name } = city;
    if (!groupedCities[country]) {
        groupedCities[country] = [];
    }
    groupedCities[country].push({ code, name });
  });
  return groupedCities;
}

export const destinationsForSelect = (origin) => {
  let destinationCodes = routes.filter(route => route.origin === origin).map(route => route.destination);
  let destinations = cities.filter(city => destinationCodes.includes(city.code));
  return citiesForSelect(destinations);
}

export const getCityNameByCode = (code) => {
  let cityName = null;
  for (let i = 0; cityName === null && i < cities.length; i++) {
    if (cities[i].code === code) {
      cityName = cities[i].name;
    }
  }
  return cityName;
}